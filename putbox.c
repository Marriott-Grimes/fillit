#include "fillit.h"

void	putbox(char *str, size_t n)
{
	size_t i;

	i = 0;
	while (i < n * n)
	{
		ft_putchar(str[i]);
		i++;
		if (i % n == 0)
			ft_putchar('\n');
	}
}
